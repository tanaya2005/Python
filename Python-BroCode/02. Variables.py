# Variables in Python are a container to store data values. Variables are created when you assign a value to it. they can be "string", integer, float, boolean

name="Tanaya"
#strings= collection of characters
print(name)
print(f"heyy this is {name}")  
# adding an f (f=format) before the string allows you to add variables in the string using {} and the variable name inside it.

#integer= whole number
age=21
print(age)
print(f"heyy this is {name} and I am {age} years old")

#float= decimal number
cgpa= 9.99
print(cgpa)
print(f"heyy this is {name} and I am {age} years old and my cgpa is {cgpa}")

#boolean= True or False
is_student=True
is_working=False
print(is_student)
print(f"heyy this is {name} and I am {age} years old and my cgpa is {cgpa} and I am a student {is_student}")
print(f"heyy this is {name} and I am {age} years old and my cgpa is {cgpa} and I am working {is_working}")
